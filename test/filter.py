
# Исходная матрица m в виде списка списков
m = [
    [1, 2, 3],
    [-1, 0, 2],
    [4, -5, 6]
]

# Шаг 1: Выбор строк без отрицательных чисел с помощью filter и min
filtered_rows = list(filter(lambda row: min(row) >= 0, m))

# Шаг 2: Создание новой транспонированной матрицы m_r1 из выбранных строк с помощью zip
m_r1 = tuple(zip(*m))

# Вывод результата
print(filtered_rows)
print(m_r1)